CREATE TABLE persona();
ALTER TABLE persona
ADD COLUMN ID_PERSONA SERIAL PRIMARY KEY NOT NULL;

CREATE TABLE directivo(
ID_DIRECTIVO SERIAL PRIMARY KEY NOT NULL,
CARGO VARCHAR(100) NOT NULL,
NOMBRE VARCHAR(100) NOT NULL,
TELEFONO BIGINT NOT NULL
)INHERITS (persona);

CREATE TABLE jugador(
ID_JUGADOR SERIAL PRIMARY KEY NOT NULL,
NACIONALIDAD VARCHAR(100) NOT NULL,
NOMBRE_JUGADOR VARCHAR(100) NOT NULL,
POSICION VARCHAR(100) NOT NULL,
CODIGO_JUGADOR BIGINT NOT NULL,
TELEFONO BIGINT NOT NULL,
FECHA_NACIMIENTO DATE NOT NULL
)INHERITS (persona);




CREATE TABLE equipo(
ID_EQUIPO SERIAL PRIMARY KEY NOT NULL,
NOMBRE_EQUIPO VARCHAR (100) NOT NULL,
CIUDAD VARCHAR(160) NOT NULL,
ANIO_FUNDACION DATE NOT NULL,
ID_JUGADOR SERIAL REFERENCES jugador(ID_JUGADOR),
ID_ESTADIO SERIAL REFERENCES estadio(ID_ESTADIO)	
)

CREATE TABLE partidos(
ID_PARTIDO SERIAL PRIMARY KEY NOT NULL,
NOMBRE_EQUIPO_RIVAL VARCHAR(100) NOT NULL,
CIUDAD_PARTIDO VARCHAR(100) NOT NULL,
FECHA_PARTIDO DATE NOT NULL
)

CREATE TABLE estadisticas(
TIROS_ARCO SMALLINT NOT NULL,
TIROS_ESQUINA SMALLINT NOT NULL,
POSESION_BALON FLOAT NOT NULL,
GOLES SMALLINT NOT NULL,
ID_PARTIDO SERIAL REFERENCES partidos(ID_PARTIDO)
)

CREATE TABLE edificios();

ALTER TABLE edificios
ADD COLUMN ID_EDIFICIOS SERIAL PRIMARY KEY NOT NULL;

CREATE TABLE estadio(
ID_ESTADIO SERIAL PRIMARY KEY NOT NULL,
NOMBRE_ESTADIO VARCHAR (100) NOT NULL,
CIUDAD VARCHAR(100) NOT NULL,
AFORO INT NOT NULL
)INHERITS (edificios);

CREATE TABLE centro_deportivo(
ID_CD INT NOT NULL
)INHERITS (edificios);

CREATE TABLE area_entrenamiento(
LUGAR VARCHAR (100) NOT NULL
)INHERITS (edificios);


